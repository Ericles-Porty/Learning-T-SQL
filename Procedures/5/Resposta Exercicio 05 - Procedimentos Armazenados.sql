CREATE PROCEDURE SP_CALCULA_IR(@RENDA NUMERIC(10,2), @IMPOSTO NUMERIC(10,2) OUTPUT)
AS
BEGIN
   IF @RENDA <= 1372.81
      SET @IMPOSTO  = 0.0
   ELSE
      IF @RENDA <= 2743.25 
         SET @IMPOSTO = (@RENDA * 0.15) - 205.92
      ELSE
         SET @IMPOSTO = (@RENDA * 0.275) - 548.82
END

--Teste

DECLARE @IMPOSTO NUMERIC(10,2)
EXEC SP_CALCULA_IR 1500, @IMPOSTO OUTPUT
PRINT @IMPOSTO