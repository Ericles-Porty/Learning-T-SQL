BEGIN
   SET @PENDENCIA = 'SEM PENDENCIA'
   IF @TELEFONE IS NULL OR @ENDERECO IS NULL 
      OR @SALARIO IS NULL
	SET @PENDENCIA = 'EXISTE PENDENCIA'
   UPDATE TB_FUNCIONARIO
   SET PENDENCIA = @PENDENCIA
   WHERE CURRENT OF C_FUNCIONARIO
