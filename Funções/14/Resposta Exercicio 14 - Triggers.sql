CREATE OR ALTER FUNCTION dbo.SP_CALCULA_IMPOSTO(@SALARIO NUMERIC(10,2))
RETURNS NUMERIC(10,2)
AS
BEGIN
	IF @SALARIO < 1372.81		
		RETURN 0 
	ELSE
		IF @SALARIO < 2743.25
			RETURN @SALARIO  * 0.15 - 205.92 
		
	RETURN @SALARIO * 0.275 - 548.82
END
go

DECLARE @IMPOSTO NUMERIC(10,2)
EXEC @IMPOSTO = dbo.SP_CALCULA_IMPOSTO 1500
